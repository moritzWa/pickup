export const run = async () => {
    // get all tokens
    const algoliaTokensResp = await algolia.tokens.fetchAll();

    if (algoliaTokensResp.isFailure()) {
        console.error(algoliaTokensResp.error);
        throw algoliaTokensResp.error;
    }

    const algoliaTokens = algoliaTokensResp.value;

    const contractAddresses = new Set(POLITICIANS.map((t) => t.address));

    const dataByContractAddress = keyBy(
        POLITICIANS,
        (t: Politician) => t.address
    );

    const algoliaByContractAddress = keyBy(
        algoliaTokens,
        (t) => t.contractAddress
    );

    const tokensResponse = await tokenRepo.find({
        where: {
            contractAddress: In(Array.from(contractAddresses)),
        },
    });

    const tokens = tokensResponse.value;

    const strictTokensResponse = await _getTokens("/strict");
    const strictTokens = keyBy(strictTokensResponse.value, (t) => t.address);

    debugger;

    for (const token of tokens) {
        const data: Politician =
            dataByContractAddress[token.contractAddress || ""];
        const jupToken = strictTokens[token.contractAddress];
        const algoliaToken = algoliaByContractAddress[token.contractAddress];

        if (!data) {
            console.log(`No data found for ${token.contractAddress}`);
        }

        const name = data.name ?? token.name;

        const entryResponse = await pgCategoryEntryRepo.upsertForToken(
            token.id,
            {
                category: Category.Politics,
                name: name,
            }
        );

        if (entryResponse.isFailure()) {
            console.error(entryResponse.error);
        }

        const response = await tokenRepo.updateByContractAddress(
            token.contractAddress,
            {
                iconImageUrl: data.img,
                fdv: data.fdv,
                name: name,
                twitterLink: data.twitter,
                telegramLink: data.telegram,
                symbol: data.symbol,
                isStrict: !!jupToken ? true : token.isStrict,
                vol24h: data.volume24h,
            }
        );

        if (response.isFailure()) {
            debugger;
            console.error(response.error);
            continue;
        }

        const newToken = response.value;

        if (!algoliaToken) {
            console.log("algolia token is not found");
            debugger;
        }

        await algolia.tokens.save([
            {
                ...algoliaToken,
                objectID: newToken.contractAddress,
                name: newToken.name,
                symbol: newToken.symbol,
                contractAddress: newToken.contractAddress,
                iconImageUrl: newToken.iconImageUrl,
                coingeckoId: newToken.coingeckoId,
                fdv: newToken.fdv,
                vol24h: newToken.vol24h,
                isStrict: newToken.isStrict,
            },
        ]);
    }

    console.log("DONE");

    debugger;
};

connect()
    .then(() => run())
    .then(() => process.exit(0))
    .catch((err) => {
        console.error("===== ERROR RUNNING BACKFILL =====");
        console.error(err);
        process.exit(1);
    });

